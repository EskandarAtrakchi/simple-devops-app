const assert = require("assert");

describe("Math tests", function () {

  it("should return 5 when adding 2 + 3", function () {
    const result = 2 + 3;
    assert.equal(result, 5);
  });

  it("should return 8 when multiplying 4 * 2", function () {
    const result = 4 * 2;
    assert.equal(result, 8);
  });

}); 
